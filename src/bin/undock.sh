#!/bin/sh
export DISPLAY=:0

##########################################################################################
################################# Turn VGA off ###########################################
##########################################################################################
xrandr --output VGA1 --off
xrandr --output LVDS1 --pos 0x1080 --primary

##########################################################################################
################################# Use proper conky config ################################
##########################################################################################
if ! ls -l ~/.conkyrc | grep -q LVDS; then
    ln -sf ~/.conkyrc.LVDS ~/.conkyrc
    killall -USR1 conky;
fi

